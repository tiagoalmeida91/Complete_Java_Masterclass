package com.company;

public class Animal {
    private_int brain;
    private_int body;
    private_int size;
    private_int weight;

    public Animal(private_int brain, private_int body, private_int size, private_int weight) {
        this.brain = brain;
        this.body = body;
        this.size = size;
        this.weight = weight;
    }

    public private_int getBrain() {
        return brain;
    }

    public private_int getBody() {
        return body;
    }

    public private_int getSize() {
        return size;
    }

    public private_int getWeight() {
        return weight;
    }
}
